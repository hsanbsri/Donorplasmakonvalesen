        <h1 class="red">Fungsi Donor Plasma</h1>
            <p>Ia diperkaya dengan protein yang membantu melawan infeksi (bagian dari sistem kekebalan) dan membantu darah dalam pembekuan (bagian dari sistem hemostatik). Plasma AB plasma yang dikumpulkan dari donor golongan darah AB. Ini dianggap “universal donor” plasma karena cocok untuk semua penerima, terlepas dari golongan darah. Karena nilainya sebagai komponen transfusi, kadang-kadang disebut sebagai “emas cair.”

            Produk plasma darah yang tersedia di Amerika Serikat termasuk fresh frozen plasma dan thawed plasma yang dapat disimpan pada 33,8 hingga 42,8 ° F (1 hingga 6 ° C) hingga lima hari 5). Plasma darah mengandung semua faktor koagulasi. Infus plasma beku segar dapat digunakan untuk pembalikan efek antikoagulan. Plasma yang dicairkan memiliki tingkat yang lebih rendah dari faktor V dan VIII dan tidak diindikasikan pada pasien dengan koagulopati konsumsi (koagulasi intravaskular difus)</p>
